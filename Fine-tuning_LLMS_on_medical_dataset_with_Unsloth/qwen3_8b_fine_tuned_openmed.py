def format_conversation(example):
    conversation = ""
    for msg in example["messages"]:
        role = msg["role"]
        content = msg["content"]
        conversation += f"{role}: {content}\n"
    return {"text": conversation}

formatted_dataset = dataset.map(format_conversation)

